            <p class="blog-post-meta">Junio 02, 2018<a href="#">Judas Tadeo</a></p>
            <p align="justify">Judas Tadeo fue, según diversos textos neotestamentarios (Evangelios, Hechos de los Apóstoles), uno de los discípulos de Jesús de Nazaret, que formaba parte del grupo de «los doce» apóstoles. Se le menciona en los Evangelios como «hermano de Jesús». También se le llama simplemente «Tadeo», o «Judas de Santiago», aunque la identificación entre «Tadeo» (en los Evangelios de Mateo y de Marcos) y «Judas de Santiago» (en el Evangelio de Lucas y en los Hechos de los Apóstoles) es discutida por los especialistas. En todos los casos, parece existir la tendencia de acompañar el nombre de «Judas» con alguna especificación, quizá por la preocupación de los escritores de aquellos textos por diferenciar a Judas Tadeo de Judas Iscariote,2​ el apóstol a quien se atribuye haber traicionado a Jesús.</p>

            <p>El nombre «Judas» es una palabra hebrea (יהודה, Yehuda), que significa alabanzas sean dadas a Dios. «Tadeo», término proveniente del idioma arameo, significa el valiente, hombre de pecho robusto. También se le llamó «Lebbeo», que significa hombre de corazón tierno.</p>
            <br>

            <blockquote>
              <p>Junto con Simón el Cananeo, Judas Tadeo era uno de los apóstoles considerados como más judaizantes dentro del grupo de «los Doce»..</p>
            </blockquote>
            <p>Según el Evangelio de Juan, fue testigo privilegiado de la Última Cena, durante la cual tuvo una participación activa explícita. La tradición eclesiástica le atribuye la autoría de la epístola de Judas, punto también debatido por los biblistas.</p>

<p align="justify">La escasez de datos sobre Judas Tadeo y algunas identificaciones equívocas de su persona se vieron reflejadas en la variedad iconográfica que lo caracteriza.​ Se le representó con una maza o un mazo, herramienta con la que -según la tradición católica- sufrió martirio (hasta el siglo XIV se lo personificó con frecuencia con alabarda o hacha, e incluso con espada).La «regla doblada», con la que también se le suele representar, es una estilización del sable shamsir de origen persa, arma con la que asimismo se atribuye su decapitación. A menudo sus representaciones portan una imagen de Jesús, a veces con forma de medallón, en el pecho, en recuerdo de la leyenda según la cual este apóstol habría llevado el mandylion a la corte del rey Abgar V de Edesa, para sanarle. (En realidad, quien portaba el mandylion era Tadeo de Edesa, uno de los setenta y dos discípulos mencionados en <strong>Lucas 10:1-24</strong>, pero para cuando fue descubierto el error, la iconografía del medallón en el pecho de Judas Tadeo ya se había popularizado). También se le representa con una llama de fuego sobre su cabeza, significando su presencia en Pentecostés, y un rollo en representación de la epístola de Judas, uno de los libros canónicos, que la tradición eclesiástica tendió a atribuirle. En el simbolismo medieval, se consideró la piedra preciosa «crisoprasa» como atributo del apóstol Judas Tadeo.</p>

