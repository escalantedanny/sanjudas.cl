            <h2 class="blog-post-title">Devoci&oacute;n San Judas Tadeo Chile</h2>
            <p class="blog-post-meta">Junio 02, 2018<a href="#">Historia</a></p>

            <p align="justify">La devoción a San Judas Tadeo en Chile llegó junto a los padres claretianos, procedentes de España, quienes se establecieron en 1870 en Santiago. Tras su llegada al país, expandieron la congregación a otras ciudades del país como Antofagasta, Copiapó, La Serena, Valparaíso, Curicó y Temuco, donde establecieron colegios, dispensarios, comedores e internados. En 1879 se inauguró y bendijo la <strong>Basílica del Corazón de María</strong>, emplazada en el barrio San Diego, en el centro de Santiago, que es la sede nacional de la orden.</p>

            <p align="justify">Su imagen, venerada por la orden claretiana, y replicada en las iglesias establecidas por la orden en el país, fue traída por un sacerdote español en 1910, cuando una feligresa de la Basílica, Elena Correa, devota de San Judas Tadeo, propuso la colocación de un altar en honor al santo en la Basílica, el cual fue traído por la orden del Oratorio de San Felipe Neri, quienes dejaron el país. En 1922 se rezó la primera novena a San Judas Tadeo, expandiendo rápidamente la devoción a otras ciudades del país, especialmente en la que los claretianos cuentan con su presencia, erigiendo altares e imprimiendo novenas y estampas en la imprenta que la orden mantuvo en Santiago..</p>
            <blockquote>
              <p align="justify">Actualmente, los 28 de cada mes, millares de fieles se reúnen en la Basílica para participar en las misas y dar gracias al santo por los favores recibidos. El día de su fiesta, el 28 de octubre, se realizan misas desde las 8 de la mañana hasta las 7 de la tarde, siendo esta última presidida por el arzobispo de Santiago, además de realizar una procesión por las inmediaciones de la iglesia, siendo acompañada por una multitud proveniente de distintos puntos de la ciudad y el país...</p>
            </blockquote>
            <p align="justify">Además de Santiago, a San Judas Tadeo se le venera en la localidad de Malloa, en la Región de O'Higgins, de la cual es su santo patrono. Millares de peregrinos de la localidad y sus alrededores se reúnen el 28 de octubre para rendir culto al santo, cuya devoción se originó en 1887, cuando un sacerdote italiano trajo una pintura con la imagen del santo tras enterarse de un brote de cólera que se propagó en la zona, encomendando a San Judas Tadeo para la protección de los enfermos, los cuales han sido curados tras realizar una procesión desde Pelequén hasta Malloa, donde fue colocada en el altar mayor de la iglesia local..</p>
